number1 = int(input("what is the first number ?  "))
number2 = int(input("what is the secend number ?  "))
operator = input("what do you what to do? (+, -, *, /)  ")
if operator == "-":
    print(number1 - number2)
if operator == "+":
    print(number1 + number2)
if operator == "*":
    print(number1 * number2)
if operator == "/":
    print(number1 / number2)